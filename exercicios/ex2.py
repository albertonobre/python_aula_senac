# Faça um algoritmo que receba dois números e ao final mostre a soma, subtração, multiplicação e a divisão dos
#  números lidos.

n1 = int(input("Digite o primeiro número:"))
n2 = int(input("Digite o segundo número:"))

print("A soma entre os números é:", n1+n2)

print("A subtração entre os números é:", n1-n2)

print("A multiplicação entre os números é:", n1*n2)

print("A divisão entre os números é:", n1/n2)
